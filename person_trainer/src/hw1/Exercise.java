public class Exercise {
	String name;
	String level;
	int repTime;
	int repCount;
	int calories;
	String exerciseNotes;
	
	Exercise(String name, String level, int repTime, int repCount, int calories, String exerciseNotes) {
		this.name = name;
		this.level = level;
		this.repTime = repTime;
		this.repCount = repCount;
		this.calories = calories;
		this.exerciseNotes = exerciseNotes;
	}
}
